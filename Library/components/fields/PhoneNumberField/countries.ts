/**
 * Converts an ISO 3166-1 alpha-2 country code into its flag emoji using
 * regional indicator symbols, so no image assets are required.
 */
export function countryCodeToFlag(code: string): string {
  if (!code || code.length !== 2) {
    return "🏳️";
  }

  const base = 127397;
  return code
    .toUpperCase()
    .replace(/[^A-Z]/g, "")
    .split("")
    .map((char) => String.fromCodePoint(base + char.charCodeAt(0)))
    .join("");
}

