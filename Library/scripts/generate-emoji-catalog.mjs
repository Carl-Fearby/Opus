import { createRequire } from "node:module";
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const data = require("@emoji-mart/data");

const __dirname = dirname(fileURLToPath(import.meta.url));
const outputPath = join(__dirname, "../lib/emojiCatalog.generated.ts");

function toEntry(id) {
  const record = data.emojis[id];
  const native = record?.skins?.[0]?.native;
  if (!record || !native) {
    return null;
  }

  const keywords = new Set();
  for (const keyword of record.keywords ?? []) {
    keywords.add(keyword.replaceAll("_", " "));
  }
  keywords.add(record.id.replaceAll("_", " "));
  if (record.name) {
    keywords.add(record.name.toLowerCase());
  }

  return {
    emoji: native,
    keywords: [...keywords],
  };
}

function fromIds(ids) {
  const entries = [];
  const seen = new Set();

  for (const id of ids) {
    const entry = toEntry(id);
    if (!entry || seen.has(entry.emoji)) {
      continue;
    }
    seen.add(entry.emoji);
    entries.push(entry);
  }

  return entries;
}

function getCategoryIds(categoryId) {
  return data.categories.find((category) => category.id === categoryId)?.emojis ?? [];
}

const peopleIds = getCategoryIds("people");
const peopleSplitIndex = Math.max(peopleIds.indexOf("wave"), 0);
const symbolsIds = [...getCategoryIds("symbols"), ...getCategoryIds("flags")];

const categories = [
  { id: "smileys", label: "Smileys", iconName: "face-smile", ids: peopleIds.slice(0, peopleSplitIndex) },
  { id: "people", label: "People", iconName: "user", ids: peopleIds.slice(peopleSplitIndex) },
  { id: "nature", label: "Nature", iconName: "leaf", ids: getCategoryIds("nature") },
  { id: "food", label: "Food", iconName: "utensils", ids: getCategoryIds("foods") },
  { id: "activities", label: "Activities", iconName: "futbol", ids: getCategoryIds("activity") },
  { id: "travel", label: "Travel", iconName: "plane", ids: getCategoryIds("places") },
  { id: "objects", label: "Objects", iconName: "lightbulb", ids: getCategoryIds("objects") },
  { id: "symbols", label: "Symbols", iconName: "heart", ids: symbolsIds },
].map((category) => ({
  ...category,
  emojis: fromIds(category.ids),
}));

const total = categories.reduce((sum, category) => sum + category.emojis.length, 0);

const file = `// Generated by scripts/generate-emoji-catalog.mjs — do not edit by hand.
import type { EmojiCategory } from "./emojiCatalog.types";

export const generatedEmojiCategories = ${JSON.stringify(categories, null, 2)} as const satisfies readonly EmojiCategory[];

export const generatedEmojiCount = ${total};
`;

writeFileSync(outputPath, file, "utf8");

for (const category of categories) {
  console.log(`${category.id}: ${category.emojis.length}`);
}
console.log(`total: ${total}`);
console.log(`wrote ${outputPath}`);
